package model;

public class Calculadora {
	
	public Calculadora() {
		
	}
	
	public double somar(int n1, int n2) {
		double somar = (n1+n2);
		return somar;
	}
	public double subtrair(int n1, int n2) {
		double subtrair = (n1-n2);
		return subtrair;
	}
	public double multiplicar(int n1, int n2) {
		double multiplicar = (n1*n2);
		return multiplicar;
	}
	public double dividir(int n1, int n2) {
		double dividir = (n1/n2);
		return dividir;
	}

}
